# Las operaciones matematicas no difieren mucho de otros lenguajes

a = 30 
b = 10

# suma
result_sum = a + b
print(result_sum)

# resta
result_rest = a - b
print(result_rest)

# multiplicacion
result_mult = a * b
print(result_mult)

# division
result_div = a / b
print(result_div)

# modulo
result_mod = a % b
print(result_mod)


# Division entera //
c = 20
d = 3
result_div_ent = c // d
result_div_norm = c / d
print(result_div_ent) # el resultado es redondeado a entero a pesar de que es flotante
print(result_div_norm) # el resultado es decimal o flotante

# potenciacion **
result_pot = c ** d
print(result_pot)